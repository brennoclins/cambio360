const tabColors: Record<CurrencyCategory, string> = {
  principais: 'blue',
  'america-latina': 'emerald',
  europa: 'indigo',
  'asia-pacifico': 'violet',
  'oriente-medio-africa': 'amber',
  crypto: 'orange',
  commodities: 'yellow',
  ptax: 'cyan',
  turismo: 'pink',
}

function activeClasses(color: string): string {
  const map: Record<string, string> = {
    blue: 'bg-blue-600 text-white shadow-sm',
    emerald: 'bg-emerald-600 text-white shadow-sm',
    indigo: 'bg-indigo-600 text-white shadow-sm',
    violet: 'bg-violet-600 text-white shadow-sm',
    amber: 'bg-amber-600 text-white shadow-sm',
    orange: 'bg-orange-600 text-white shadow-sm',
    yellow: 'bg-yellow-600 text-white shadow-sm',
    cyan: 'bg-cyan-600 text-white shadow-sm',
    pink: 'bg-pink-600 text-white shadow-sm',
  }
  return map[color] ?? 'bg-blue-600 text-white shadow-sm'
}

export function CategoryTabs({ active, onChange }: CategoryTabsProps) {
  return (
    <div
      className="flex gap-1 p-1 bg-white rounded-xl shadow-xs ring-1 ring-slate-200/60 overflow-x-auto scrollbar-none"
      role="tablist"
    >
      {CATEGORIES.map(cat => {
        const color = tabColors[cat.key]
        const isActive = active === cat.key
        return (
          <button
            key={cat.key}
            type="button"
            role="tab"
            aria-selected={isActive}
            onClick={() => onChange(cat.key)}
            className={`flex items-center gap-1.5 px-3 py-2 text-xs font-medium rounded-lg transition-all cursor-pointer whitespace-nowrap ${
              isActive
                ? activeClasses(color)
                : 'text-slate-500 hover:text-slate-700 hover:bg-slate-50'
            }`}
          >
            <svg className="w-3.5 h-3.5 shrink-0" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
              <path strokeLinecap="round" strokeLinejoin="round" d={iconPaths[cat.key]} />
            </svg>
            {cat.label}
          </button>
        )
      })}
    </div>
  )
}
